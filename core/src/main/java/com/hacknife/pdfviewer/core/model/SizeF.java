package com.hacknife.pdfviewer.core.model;

public class SizeF {

    float width;
    float height;

    public SizeF(float width, float height) {
        this.width = width;
        this.height = height;
    }

    public float getWidth() {
        return width;
    }

    public float getHeight() {
        return height;
    }
}
